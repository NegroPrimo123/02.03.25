CREATE TABLE Водитель (
    ID INT PRIMARY KEY IDENTITY(1,1),
    Имя NVARCHAR(50),
    Фамилия NVARCHAR(50),
    Отчество NVARCHAR(50),
    Возраст INT,
    Стаж INT
);

CREATE TABLE Контакт (
    ID INT PRIMARY KEY IDENTITY(1,1),
    ID_Водителя INT FOREIGN KEY REFERENCES Водитель(ID),
    Телефон NVARCHAR(15),
    Адрес NVARCHAR(100)
);

CREATE TABLE Автомобиль (
    ID INT PRIMARY KEY IDENTITY(1,1),
    Номер NVARCHAR(15) UNIQUE,
    Марка NVARCHAR(50),
    Грузоподъемность DECIMAL(10, 2),
    Год_выпуска INT,
    Дата_последней_профилактики DATE
);

CREATE TABLE Заказ (
    ID INT PRIMARY KEY IDENTITY(1,1),
    Адрес_начала NVARCHAR(100),
    Адрес_конца NVARCHAR(100),
    Дата_выезда DATE,
    Время TIME,
    Количество_мест INT,
    Общий_вес DECIMAL(10, 2),
    Цель NVARCHAR(100)
);

CREATE TABLE Перевозка (
    ID INT PRIMARY KEY IDENTITY(1,1),
    ID_Заказа INT FOREIGN KEY REFERENCES Заказ(ID),
    ID_Водителя INT FOREIGN KEY REFERENCES Водитель(ID),
    ID_Автомобиля INT FOREIGN KEY REFERENCES Автомобиль(ID),
    Дата_начала DATE,
    Дата_окончания DATE
);




